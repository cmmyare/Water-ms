    private void serActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_serActionPerformed
        String sql = "select * from address where add_no ='" + serTex.getText() + "'";
        co.search(sql);
        try {

            if (co.rs.next()) {
                id = co.rs.getString("add_no");
                dist.setText(co.rs.getString("district"));
                villa.setText(co.rs.getString("village"));
                zone.setText(co.rs.getString("zone"));
                serTex.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "record not found");
            }
        } catch (Exception exp) {
            JOptionPane.showMessageDialog(null, exp.getMessage());
        }
    }//GEN-LAST:event_serActionPerformed

