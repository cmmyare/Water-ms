    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed

        if (dist.getText().equals("") || villa.getText().equals("") || zone.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Fill all the blanks");
        } else {
            String oper = "insert";
            String sql = "call add_pro('" + dist.getText() + "','" + villa.getText() + "','" + zone.getText() + "','" + oper + "',null)";
            co.setSQL(sql);
            clear();
            display();
            dist.requestFocus();
        }
    }//GEN-LAST:event_saveActionPerformed

